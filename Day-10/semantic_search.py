def reciprocal_rank(ranked_ids, relevant_ids):
    """
    Computes the reciprocal rank for a single query.
    Returns 1/rank of the first relevant document, or 0 if none found.
    """
    for rank, doc_id in enumerate(ranked_ids, start=1):
        if doc_id in relevant_ids:
            return 1.0 / rank
    return 0.0


def dcg_at_k(ranked_ids, graded_ids, k):
    """
    Computes Discounted Cumulative Gain at cutoff k.
    DCG@k = sum( grade_i / log2(rank_i + 1) ) for i in 1..k
    """
    dcg = 0.0
    for rank, doc_id in enumerate(ranked_ids[:k], start=1):
        grade = graded_ids.get(doc_id, 0)
        dcg += grade / math.log2(rank + 1)
    return dcg


def ndcg_at_k(ranked_ids, graded_ids, k):
    """
    Computes Normalized DCG at cutoff k.
    Divides actual DCG by the ideal DCG (best possible ranking of the grades).
    Returns 0.0 if the ideal DCG is zero (no relevant docs exist).
    """
    # Ideal ranking: sort available grades descending
    ideal_grades = sorted(graded_ids.values(), reverse=True)
    ideal_ranking = list(graded_ids.keys())  # placeholder ids — only grades matter
    ideal_dcg = sum(
        grade / math.log2(rank + 2)  # rank+2 because enumerate starts at 0
        for rank, grade in enumerate(ideal_grades[:k])
    )
    if ideal_dcg == 0.0:
        return 0.0
    actual_dcg = dcg_at_k(ranked_ids, graded_ids, k)
    return actual_dcg / ideal_dcg


