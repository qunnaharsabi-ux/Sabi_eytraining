# EXTENSION C: Search Evaluation Metrics — MRR & NDCG
# =============================================================================
# Introduces a ground-truth test set and formally evaluates all three search
# engines (sparse, dense, hybrid) using standard IR metrics:
#
#   MRR  (Mean Reciprocal Rank)
#        Measures how highly the FIRST relevant document is ranked.
#        MRR = mean( 1 / rank_of_first_relevant_doc ) across all queries.
#        Score of 1.0 → every query's top result is relevant.
#        Score of 0.5 → first relevant doc is on average at rank 2.
#
#   NDCG (Normalized Discounted Cumulative Gain) @k
#        Measures the quality of the ENTIRE top-k ranking, rewarding systems
#        that put highly-relevant docs near the top.
#        Uses a log2 discount: documents ranked lower contribute less.
#        Score of 1.0 → perfect ranking; 0.0 → no relevant docs in top-k.
#
# Ground-truth format:
#   Each entry is a dict with:
#     "query"        – the natural-language query string
#     "relevant_ids" – list of doc IDs considered relevant (binary, grade=1)
#     "graded_ids"   – dict {doc_id: grade} for NDCG (grade 2=highly relevant,
#                      1=relevant, 0=not relevant)
# =============================================================================

