def evaluate_engine(retrieve_fn, ground_truth, k=3, engine_name="Engine"):
    """
    Runs retrieve_fn over every test query, computes per-query MRR and NDCG@k,
    then prints a detailed breakdown table and macro-averaged summary.

    Args:
        retrieve_fn   : callable(query, top_n) → list[doc_id]
        ground_truth  : list of dicts with keys query/relevant_ids/graded_ids
        k             : cutoff for NDCG (default 3)
        engine_name   : label used in the printed header
    """
    rr_scores = []
    ndcg_scores = []

    header = f"\n{'=' * 65}\n  {engine_name.upper()} — Evaluation @ k={k}\n{'=' * 65}"
    print(header)
    print(f"{'Query':<42} {'RR':>6}  {'NDCG@k':>7}")
    print("-" * 58)

    for item in ground_truth:
        query = item["query"]
        relevant_ids = set(item["relevant_ids"])
        graded_ids = item["graded_ids"]

        ranked = retrieve_fn(query, top_n=max(k, 10))

        rr = reciprocal_rank(ranked, relevant_ids)
        ndcg = ndcg_at_k(ranked, graded_ids, k)

        rr_scores.append(rr)
        ndcg_scores.append(ndcg)

        # Truncate long queries for display
        short_q = (query[:39] + "…") if len(query) > 40 else query
        print(f"  {short_q:<40} {rr:>6.4f}  {ndcg:>7.4f}")

    mrr = np.mean(rr_scores)
    mean_ndcg = np.mean(ndcg_scores)

    print("-" * 58)
    print(f"  {'MACRO AVERAGE':<40} {mrr:>6.4f}  {mean_ndcg:>7.4f}")
    print(f"\n  MRR         = {mrr:.4f}  (higher → first relevant result is ranked earlier)")
    print(f"  Mean NDCG@{k} = {mean_ndcg:.4f}  (higher → better overall top-{k} ranking quality)")
    print("=" * 65)

    return {"MRR": mrr, f"NDCG@{k}": mean_ndcg}


