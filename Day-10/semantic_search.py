def retrieve_sparse(query, top_n=6):
    """Returns list of doc_ids ranked by BM25 score (descending)."""
    results = sparse_search(query, top_n=top_n)
    return [doc_id for doc_id, _ in results]


def retrieve_dense(query, top_n=6):
    """Returns list of doc_ids ranked by cosine similarity (descending)."""
    results = dense_search(query, top_n=top_n)
    return [doc_id for doc_id, _ in results]


def retrieve_hybrid(query, top_n=6):
    """Returns list of doc_ids ranked by RRF score (descending)."""
    sparse_res = sparse_search(query, top_n=10)
    dense_res = dense_search(query, top_n=10)
    fused = reciprocal_rank_fusion(sparse_res, dense_res, k=60, top_n=top_n)
    return [doc_id for doc_id, _ in fused]


